// Helper function to get products by category
export const getProductsByCategory = (category: string) => {
  if (category === 'all') {
    return products;
  }
  
  // Case-insensitive comparison
  return products.filter(product => 
    product.category.toLowerCase() === category.toLowerCase()
  );
};

